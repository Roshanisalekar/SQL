--                                         sum function 
-------------------------------------------------------------------------------------------------------------------
--    The SQL SUM() Function  : 
-- The SUM() function returns the total sum of a numeric column.
-------------------------------------------------------------------------------------------------------------------
-- syntax : 
-- SELECT SUM(column_name)
-- FROM table_name
-- WHERE condition;
------------------------------------------------------------------------------------------------------------------

select sum(salary) from employee;
------------------------------------------------------------------------------------------------------------------
-- use alias 
select sum(salary) as sum_salary from employee;
------------------------------------------------------------------------------------------------------------------
-- use group by

select sum(salary) as sum_salary ,id  from employee group by id;

------------------------------------------------------------------------------------------------------------------------
 
-- use where
select sum(salary) from employee where age >21;
------------------------------------------------------------------------------------------------------------------------

