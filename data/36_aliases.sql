--                                         aliases 
---------------------------------------------------------------------------------------------------------------------
-- SQL aliases are used to a temporary name.
---------------------------------------------------------------------------------------------------------------------
-- Syntax  :  
---------------------------------------------------------------------------------------------------------------------
-- alias is used on column:

-- SELECT column_name AS alias_name
-- FROM table_name;

select salary as expectation_salary from
emp;
---------------------------------------------------------------------------------------------------------------------
--  alias is used on table:

-- SELECT column_name(s)
-- FROM table_name AS alias_name;

select salary from emp as emppp;


---------------------------------------------------------------------------------------------------------------------

-- Using Aliases With a Space Character

-- use quates '  ' ," '
select salary as 'expectation salary' from emp;
---------------------------------------------------------------------------------------------------------------------
-- use []  

select sname AS [Employee names] from emp;
